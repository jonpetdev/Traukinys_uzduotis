public class Keleiviai {

    String vardas;
    String pavarde;
    int metai;

    Keleiviai(String vardas, String pavarde, int metai){
        this.vardas=vardas;
        this.pavarde=pavarde;
        this.metai=metai;
    }

    public String getVardas() {
        return vardas;
    }

    public void setVardas(String vardas) {
        this.vardas = vardas;
    }

    public String getPavarde() {
        return pavarde;
    }

    public void setPavarde(String pavarde) {
        this.pavarde = pavarde;
    }

    public int getMetai() {
        return metai;
    }

    public void setMetai(int metai) {
        this.metai = metai;
    }


}
